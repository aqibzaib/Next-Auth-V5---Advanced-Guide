import authConfig from "@/auth.config";
import NextAuth from "next-auth";
import {
  DEFAULT_LOGIN_REDIRECT,
  apiAuthPrefix,
  authRoutes,
  publicRoutes,
} from "@/routes";
import { NextResponse } from "next/server";

const { auth } = NextAuth(authConfig);

export default auth((req) => {
  const { nextUrl } = req;
  const isLoggedIn = !!req.auth;
  const isApiAuthRoutes = nextUrl.pathname.startsWith(apiAuthPrefix);
  const isPublicRoutes = publicRoutes.includes(nextUrl.pathname);
  const isAuthRoutes = authRoutes.includes(nextUrl.pathname);

  if (isApiAuthRoutes) {
    return null;
  }

  // if logged in & trying to visit /auth/login or /auth/register â†’ redirect away
  if (isAuthRoutes && isLoggedIn) {
    return NextResponse.redirect(
      new URL(DEFAULT_LOGIN_REDIRECT, nextUrl.origin)
    );
  }

  // if NOT logged in & trying to visit a protected route (not public, not auth page)
  if (!isLoggedIn && !isPublicRoutes && !isAuthRoutes) {
    return NextResponse.redirect(new URL("/auth/login", nextUrl.origin));
  }

  return null;
});

export const config = {
  matcher: [
    // Skip Next.js internals and all static files, unless found in search params
    "/((?!_next|[^?]*\\.(?:html?|css|js(?!on)|jpe?g|webp|png|gif|svg|ttf|woff2?|ico|csv|docx?|xlsx?|zip|webmanifest)).*)",
    // Always run for API routes
    "/(api|trpc)(.*)",
  ],
};
